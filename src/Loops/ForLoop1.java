package Loops;

public class ForLoop1 {

	public static void main(String[] args) 
	{
		int a=10;
		System.out.println(a);
		a=a+1;
		System.out.println(a);
		a++;//increment by 1--> a=a+1
		System.out.println(a);
		++a;
		System.out.println(a);
		System.out.println("===============");
		a=a-1;
		System.out.println(a);
		a--;// decrement by 1 -->a=a-1;
		System.out.println(a);
		a=a+2;
		a=a-2;
		// a=a+2;
		// System.out.println(a);

	}

}
